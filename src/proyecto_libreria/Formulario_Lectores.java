    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed

        String Cedula = jTCedula.getText();
        String Nombre = jTNombre.getText();
        String Apellidos = jTApellidos.getText();
        String Telefono = jTTelefono.getText();
        String Direccion = jTDireccion.getText();

        try (Connection conn = Base_De_Datos_Conexion.getConnection()) {
            String sql = "INSERT INTO Lectores (Cedula, Nombre, Apellidos, Telefono, Direccion) VALUES (?,?,?,?,?)";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, Cedula);
            statement.setString(2, Nombre);
            statement.setString(3, Apellidos);
            statement.setString(4, Telefono);
            statement.setString(5, Direccion);

            statement.executeUpdate();

            JOptionPane.showMessageDialog(null, "El lector se ha agregado exitosamente");
            LectoresCarga();
            limpia();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jBAgregarActionPerformed


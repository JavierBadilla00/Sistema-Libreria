      private void LectoresCarga() {
        LectoresLista.clear();
        modelo.setRowCount(0);
 
        try (Connection conn = Base_De_Datos_Conexion.getConnection()) {
            String sql = "SELECT * FROM Lectores";
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet resultSet = statement.executeQuery();
 
            while (resultSet.next()) {
                String Cedula = resultSet.getString("Cedula");
                String Nombre = resultSet.getString("Nombre");
                String Apellidos = resultSet.getString("Apellidos");
                String Telefono = resultSet.getString("Telefono");
                String Direccion = resultSet.getString("Direccion");

               Lector l = new Lector(Cedula, Nombre, Apellidos, Telefono, Direccion);
               LectoresLista.add(l);
                modelo.addRow(new Object[]{Cedula, Nombre, Apellidos, Telefono, Direccion});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
      

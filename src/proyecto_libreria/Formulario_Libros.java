    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed

        String ID = jTID.getText();
        String Nombre = jTNombre.getText();
        String Autor = jCBAutor.getSelectedItem().toString();
        int Precio = Integer.parseInt(jTPrecio.getText());

        try (Connection conn = Base_De_Datos_Conexion.getConnection()) {
            String sql = "INSERT INTO Libros (ID, Nombre, ID_Autor, Precio) VALUES (?,?,?,?)";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, ID);
            statement.setString(2, Nombre);
            statement.setString(3, Autor);
            statement.setInt(4, Precio);

            statement.executeUpdate();

            JOptionPane.showMessageDialog(null, "El libro fue agregado exitosamente");
            LibrosCarga();
            limpia();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jBAgregarActionPerformed

    private void jBModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBModificarActionPerformed

        int TablaSeleccionada = jTablaLibros.getSelectedRow();

        if (TablaSeleccionada >=0 ){

            String ID = jTID.getText();
            String Nombre = jTNombre.getText();
            String Autor = jCBAutor.getSelectedItem().toString();
            int Precio = Integer.parseInt(jTPrecio.getText());

            try (Connection conn = Base_De_Datos_Conexion.getConnection()) {
                String sql = "UPDATE Libros SET Nombre = ?, ID_Autor = ?, Precio = ? WHERE ID = ?";
                PreparedStatement statement = conn.prepareStatement(sql);
                statement.setString(1, Nombre);
                statement.setString(2, Autor);
                statement.setInt(3, Precio);
                statement.setString(4, ID);

                statement.executeUpdate();
                JOptionPane.showMessageDialog(null, "El libro fue modificado exitosamente");
                LibrosCarga();
                limpia();
            } catch (SQLException e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jBModificarActionPerformed


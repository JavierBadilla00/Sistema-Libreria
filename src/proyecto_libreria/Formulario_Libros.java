    private void AutoresCBX (JComboBox<String> prov) throws SQLException {
    DefaultComboBoxModel<String> cbx = new DefaultComboBoxModel<>();
    prov.setModel(cbx);
    
    try (Connection conn = Base_De_Datos_Conexion.getConnection()) {
        String sql = "SELECT Nombre FROM Autores";
        PreparedStatement statement = conn.prepareStatement(sql);
        ResultSet resultSet = statement.executeQuery();

        while (resultSet.next()) {
            
            cbx.addElement(resultSet.getString("Nombre"));
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al cargar autores: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
    
     private void LibrosCarga() {
        LibrosLista.clear();
        modelo.setRowCount(0);
 
        try (Connection conn = Base_De_Datos_Conexion.getConnection()) {
            String sql = "SELECT * FROM Libros";
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet resultSet = statement.executeQuery();
 
            while (resultSet.next()) {
                String ID = resultSet.getString("ID");
                String Nombre = resultSet.getString("Nombre");
                String Autor = resultSet.getString("ID_Autor");
                int Precio = resultSet.getInt("Precio");


               Libro l = new Libro(ID, Nombre, Autor, Precio);
                modelo.addRow(new Object[]{ID, Nombre, Autor, Precio});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
     
